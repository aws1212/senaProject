function dentro(button){
    button.style.left = "1%";
    button.style.top = "5%";
    button.style.width = "90%"
    button.style.height = "90%";
}
function fuera(button){
    button.style.left = "0%";
    button.style.top = "0%";
    button.style.width = "100%"
    button.style.height = "100%";
}